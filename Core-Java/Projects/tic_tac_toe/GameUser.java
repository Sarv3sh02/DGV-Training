package tic_tac_toe;

public class GameUser {
	private String name;
	private int streak;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setStreak(int streak) {
		this.streak = streak;
	}

	public int getStreak() {
		return streak;
	}
	
}
