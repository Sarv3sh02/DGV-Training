package quiz_game;

public class QuizUser {
	private String name;
	private int marks;
	
	public QuizUser(String name, int marks) {
		this.name = name;
		this.marks = marks;
	}

	public String getName() {
		return name;
	}

	public int getMarks() {
		return marks;
	}
	
	
	
	
	

}
